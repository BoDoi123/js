// string.trim()
let s11 = "          He              llo          World           ";

console.log(s11.length);

let s12 = s11.trim();

console.log(s12.length);

console.log(s12);

// string.trimEnd()
let s13 = "          He              llo          World           ";

let s14 = s13.trimEnd();

console.log(s14);

// string.trimStart()
let s15 = "          He              llo          World           ";

let s16 = s15.trimStart();

console.log(s16);
